<b>III. PELANGGARAN BMPK</b><br>
<div>1. BPR dinyatakan melakukan pelanggaran BMPK dalam hal terdapat
</div><div>selisih lebih antara persentase Penyediaan Dana pada saat
</div><div>direalisasikan terhadap Modal BPR dengan BMPK yang
</div><div>diperkenankan. BPR tetap dinilai melanggar BMPK selama
</div><div>Pelanggaran BMPK tersebut belum diselesaikan.
</div><div>2. Modal BPR yang digunakan dalam perhitungan BMPK adalah jumlah
</div><div>Modal Inti dan Modal Pelengkap sebagaimana diatur dalam Peraturan
</div><div>Otoritas Jasa Keuangan mengenai kewajiban penyediaan Modal
</div><div>minimum dan pemenuhan Modal inti minimum BPR pada posisi bulan
</div><div>terakhir sebelum realisasi Penyediaan Dana.
</div><div>3. Dalam hal terdapat Pelanggaran BMPK berupa Penyediaan Dana
</div><div>dalam bentuk Kredit kepada satu atau lebih Peminjam Pihak Tidak
</div><div>Terkait yang merupakan bagian dari kelompok Peminjam Pihak Tidak
</div><div>Terkait, Pelanggaran BMPK dihitung berdasarkan penjumlahan
</div><div>pelanggaran atas pemberian Kredit kepada masing-masing Peminjam
</div><div>dan pelanggaran pemberian Kredit kepada satu kelompok Peminjam
</div><div>Pihak Tidak Terkait.
